let hello = 'Hello';
let world = 'World';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

// Template literals

let epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

// Multi-line strings

let lorem = 'esto es una linea \n' + 'esto es otra linea';
console.log(lorem);

let lorem2 = `Esto es una frase de mmierda
y esta es la segunda linea de la frase de mierda`;
console.log(lorem2);
